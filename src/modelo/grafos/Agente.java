package modelo.grafos;

public class Agente {

    private String cpf;
    private String bairro;

    public String getCpf() {
        return this.cpf;
    }

    public void setCpf(String novo) {
        this.cpf = novo;
    }

    public String getBairro() {
        return this.bairro;
    }

    public void setBairro(String novo) {
        this.bairro = novo;
    }
}
